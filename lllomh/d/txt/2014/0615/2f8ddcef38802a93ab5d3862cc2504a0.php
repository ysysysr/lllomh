			<p>一说到彩虹，估计不少人都见过，小编小的时候也见过，不过现在感觉就很少见了。小的时候不懂，感觉彩虹很神奇，像天空中的七彩桥一样，特别漂亮，后来长大，学了物理，知道它是一种物理现象。据报道，彩虹是气象中的光学现象。刚下过雨的天空中，还存有不少雨水滴，太阳出来了，阳光照到空中的水滴上，光线被折射然后反射，就在天空中形成了七彩的光谱——彩虹。</p>
